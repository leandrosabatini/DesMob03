class Contato {
    constructor(id, nome, celular, foto, lat, lng, createdAt) {
        this.id = id;
        this.nome = nome;
        this.celular = celular;
        this.foto = foto;
        this.lat = lat;
        this.lng = lng;
        this.createdAt = createdAt;
    }
}

export default Contato;
